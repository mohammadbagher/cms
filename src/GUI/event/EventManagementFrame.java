    void loadEventComponents(){
        eventsPanel.removeAll();
        eventsPanel.add(headerPanel, new org.netbeans.lib.awtextra.AbsoluteConstraints(0, 0, 580, 30));
        int dep = 30;
        int index = 1;
        for (Event event: EventCatalogue.getInstace().getEvents()) {
            eventsPanel.add(new EventPanel(new Integer(index++).toString(), event, this), new org.netbeans.lib.awtextra.AbsoluteConstraints(0, dep, 580, 30));
            dep += 30;
        }
        validate();
        repaint();
    }
